export function createClickArea($, book, page, images, HTTPS_HOST, country, getSupportUrl) {
  /**
   * 创建点击区域,前三页无需创建
   * @param {Object} $ =>> jquery
   * @param {Object} book =>> book对象
   * @param {Number} page =>> 当前页码，从1开始
   * @param {Array} images =>> 图片数组
   * @param {String} HTTPS_HOST =>> https域名
   * 1-[4, 6, 8, 11, 12, 15, 19, 20, 23, 24]
   * 2-[9, 16, 18, 27]
   * 3-[13]
   * 4-[5, 17, 21, 28]
   * 5-[7, 25]
   * 6-[14]
   * 7-[10, 22]
   * 8-[26]
   */
  const links = images[page - 1].links
  let pageClass = ''
  if ([4, 6, 8, 11, 12, 15, 19, 20, 23, 24].includes(page)) {
    pageClass = 'one-area'
  }
  if ([9, 16, 18, 27].includes(page)) {
    pageClass = 'two-area'
  }
  if ([13].includes(page)) {
    pageClass = 'three-area'
  }
  if ([5, 17, 21, 28].includes(page)) {
    pageClass = 'four-area'
  }
  if ([7, 25].includes(page)) {
    pageClass = 'five-area'
  }
  if ([14].includes(page)) {
    pageClass = 'six-area'
  }
  if ([10, 22].includes(page)) {
    pageClass = 'seven-area'
  }
  if ([26].includes(page)) {
    pageClass = 'eight-area'
  }
  const childArea = []
  const single = links.length === 1
  links.forEach((link, index) => {
    const number = index + 1
    let linkUrl = link.other ? link['url' + country] || link.url : `${HTTPS_HOST}${link.url}`
    if (link.isSupportUrl) {
      linkUrl = getSupportUrl(link.url, link.intercomUrl)
    }
    childArea.push(
      `<a class="area-${number}" href="${linkUrl}" onclick="setClickPoint('${page}', ${number}, ${single}, ${link.type}, ${link.number})" target="_blank"></a>`
    )
  })

  $(book)
    .find(`.page.p${page}`)
    .append(`<div class="click-area ${pageClass} click-page-${page}"></div>`)
  $(book)
    .find(`.page.p${page} .click-area.click-page-${page}`)
    .append(childArea.join(''))
}

